package cn.com.cdboost.charge.customer.vo.param;

import lombok.Getter;
import lombok.Setter;

import java.io.Serializable;
import java.util.List;

@Setter
@Getter
public class IncomeQueryParam implements Serializable {
    private static final long serialVersionUID = 4114711264286331465L;

    /**
     * 站点guid
     */
    private String projectGuid;

    /**
     * 方案guid
     */
    private String schemeGuid;
    /**
     * 站点guid集合
     */
    private List<String> projectGuids;
    /**
     * 商户guid
     */
    private String merchantGuid;
    /**
     * 物业guid
     */
    private String propertyGuid;
    /**
     * 物业guid集合
     */
    private List<String> propertyGuids;
    /**
     * 开始时间
     */
    private String beginTime;
    /**
     * 结束时间
     */
    private String endTime;
}
