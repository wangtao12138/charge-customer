package cn.com.cdboost.charge.customer.vo.info;

import lombok.Getter;
import lombok.Setter;

import java.io.Serializable;

/**
 * 客户基本信息
 */
@Getter
@Setter
public class CustomerBaseInfo implements Serializable {
    private static final long serialVersionUID = 2874228758677868701L;

    /**
     * 标识id
     */
    private Integer id;

    /**
     * 客户唯一标识
     */
    private String customerGuid;

    /**
     * 微信昵称
     */
    private String customerName;

    /**
     * 手机号
     */
    private String customerContact;

    /**
     * 是否接收短信（0不接收，1接收）
     */
    private Integer isReceiveSms;

    /**
     * 微信openId
     */
    private String webcharNo;

    /**
     * 阿里支付用户id
     */
    private String alipayUserId;

    /**
     * 阿里支付用户昵称
     */
    private String alipayNickName;

    /**
     * 车辆类别 1-电瓶车 2-电动车
     */
    private Integer carCategory;

    /**
     * 车辆功率
     */
    private Integer carPower;

    /**
     * 客户备注信息
     */
    private String remark;

    /**
     * 客户状态 1 正常 0 禁用
     */
    private Integer customerState;
}
